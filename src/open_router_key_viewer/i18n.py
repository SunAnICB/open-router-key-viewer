class DictTranslator(QTranslator):
    def __init__(self, language_code: str) -> None:
        super().__init__()
        self.language_code = language_code

    def translate(self, context: str, source_text: str, disambiguation: str | None = None, n: int = -1) -> str:  # type: ignore[override]
        _ = context, disambiguation, n
        if self.language_code == "zh_CN":
            return source_text
        return _TRANSLATIONS.get(self.language_code, {}).get(source_text, source_text)


def tr(text: str) -> str:
    return QCoreApplication.translate("app", text)


def resolve_language_code(config_value: object) -> str:
    if isinstance(config_value, str) and config_value in {code for code, _ in LANGUAGE_OPTIONS}:
        return config_value

    locale = QLocale.system().name()
    lowered = locale.lower()
    if lowered.startswith(("zh_tw", "zh_hk", "zh_mo")):
        return "zh_TW"
    if lowered.startswith("en"):
        return "en"
    return "zh_CN"
